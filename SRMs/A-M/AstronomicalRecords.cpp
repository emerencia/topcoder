  int minimalPlanets(vector <int> A, vector <int> B) {
    int n = A.size();
    int m = B.size();
    int r = n+m;
    for (int i=0;i<n;i++)
      for (int j=0;j<m;j++) {
        memset(dp,0,sizeof(dp));
        for (int k=0;k<n;k++)
          for (int l=0;l<m;l++) {
            if ((long long)A[k]*B[j] == (long long)B[l]*A[i])
              dp[k+1][l+1] = 1 + dp[k][l];
            else
              dp[k+1][l+1] = max(dp[k+1][l],dp[k][l+1]);
          }
        r = min(r,m+n-dp[n][m]);
      }
    return r;
  }
  
