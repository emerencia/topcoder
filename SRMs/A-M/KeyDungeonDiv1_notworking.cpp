  int maxKeys(vector <int> doorR, vector <int> doorG, vector <int> roomR, vector <int> roomG, vector <int> roomW, vector <int> keys) {
    int R = 1+keys[0]+accumulate(roomR.begin(),roomR.end(),0);
    int G = 1+keys[1]+accumulate(roomG.begin(),roomG.end(),0);
    R = 1201;
    G = 1201;
    vector<vector<vector<int> > > dp(2,vector<vector<int> >(R,vector<int>(G,-1)));
    int cur=0,next = 1;
    int mx = keys[0]+keys[1]+keys[2];
    dp[cur][keys[0]+600][keys[1]+600] = keys[2];
    for (int i=0;i<doorR.size();i++) {
      dp[next] = dp[cur];
      for (int r = 0;r<R;r++)
        for (int g = 0;g<G;g++) {
          int ro = r;
          int go = g;
          int w = dp[cur][r][g];
          if (w == -1) continue;
          if (go < ro) {
            if (doorG[i]>go+w) continue;
            w-=doorG[i];
            if (w < 0) {
              go+=w;
              w = 0;
            }
            if (doorR[i]>ro+w) continue;
            w-=doorR[i];
            if (w < 0) {
              ro+=w;
              w = 0;
            }
          } else {
            if (doorR[i]>ro+w) continue;
            w-=doorR[i];
            if (w < 0) {
              ro+=w;
              w = 0;
            }
            if (doorG[i]>go+w) continue;
            w-=doorG[i];
            if (w < 0) {
              go+=w;
              w = 0;
            }
          }
          ro+=roomR[i];
          go+=roomG[i];
          w+=roomW[i];
          if (ro >= 600+roomR[i] && go >= 600+roomG[i])
            mx = max(mx,w+ro+go-1200);
          dp[next][ro][go]=max(dp[next][ro][go],w);
        }
      swap(cur,next);
    }
    return mx;
  }
  
